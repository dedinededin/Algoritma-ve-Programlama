
public class Degiskenler {

	public static void main(String[] args) {
		//int tam sayilar icin
		//double ise kusuratli sayilar icin kullanilir
		int sayi = 88;
		double kusuratlisayi = 2.5;
		//char bir adet karakteri degisken olarak atamak icin kullanilir
		char karakter = 'a';
		//boolean dogruluk ve yanlislik degerlerini atamak icin kullanilir
		boolean bool = true;
		//String bir metni bir degiskene atamak icin kullanilir.
		String cumle = "Java 101";
		
		//System.out.println();
		//parantez icine yazdiginiz degisken adinin degerini ekranda gosterir
		System.out.println(sayi);
		System.out.println(kusuratlisayi);
		System.out.println(karakter);
		System.out.println(bool);
		System.out.println(cumle);
	}
}
